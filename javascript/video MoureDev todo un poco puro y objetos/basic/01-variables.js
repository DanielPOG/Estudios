//let
//Forma recomendada
/**
 * let permite:
 * iniciar la variable sin definir
 * reescribir la variable
 */
let helloWorld2='Hello javascript 2'
console.log(helloWorld2) 

helloWorld2='Hello javascript 2 de nuevo'
console.log(helloWorld2) 
//const
//Forma recomendada
/**
 * const no permite:
 * iniciar la variable sin definir
 * reescribir la variable
 */
